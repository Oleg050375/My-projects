my_status = input('Укажите свою должность: ')
print(my_status) #вывод значения переменной
print(len(my_status)) #вывод количества символов в строке
print(my_status.upper()) #вывод значения переменной в верхнем регистре
print(my_status.lower()) #вывод значения переменной в нижнем регистре
print(my_status.replace(' ','')) #вывод значения переменной без пробелов
print(my_status[0]) #вывод первого символа значения переменной
print(my_status[-1]) #вывод последнего символа значения переменной